#include <bits/stdc++.h> //jai ganesh
using namespace std;

void solution()
{
    int x, y;
    cin >> x >> y;

    if (x < y)
        cout << "YES" << endl;

    else
        cout << "NO" << endl;
}

int main()
{
    int t;
    cin >> t;
    while (t > 0)
    {
        solution();
        t--;
    }

    return 0;
}